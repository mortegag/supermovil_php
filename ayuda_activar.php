<p>-  En caso de que usted lo desee, tambien puede solicitar a SUPER MOVIL la devolucion de lo pagado, previa verificacion de su transaccion. </p>
<p>-  Nos esforzamos por proteger la información personal, los derechos y los intereses legítimos de Super Móvil  y de nuestros Usuarios;</p>


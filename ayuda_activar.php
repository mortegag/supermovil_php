<p>-  Si usted ha realizado un pago a Super Móvil , esta transacción mediante Yappy se refleja en PANEL DE CONTROL DE YAPPY COMERCIAL de la empresa.</p>
<p>-  Estos movimientos de transacciones, son la evidencia de su pago para la activación de nuestros servicios</p>

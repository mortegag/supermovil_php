<p>Última actualización:15/03/2023</p>

<p><em>POR FAVOR LEER CUIDADOSAMENTE ESTE DOCUMENTO.</em></p>
 
<p>Para activar los servicios de Super Móvil Chofer usted deberá realizar un prepago con la banca electrónica con YAPPY, 
   dicho pago le permitirá utilizar los servicios de SUPER MOVIL en todo momento.</p> 
<p><em>IMPORTANTE..!</em>: Si desea utilizar continuamente los servicios de SUPER MOVIL, mantenga un saldo positivo.</p>
<br>
 
<p><b>PRIMER PASO : Pagar la cantidad deseada con Yappy</b></p>
<p>Cuando entre en su banca en línea , vaya a la opción de transaccionar y seleccione Yappy, desde el menú de clic al botón "Enviar" y luego seleccione "Directorio".</p>
<p>En el "Directorio" puede buscar la empresa "SUPER MOVIL" y estará listo para realizar su prepago con la cantidad  deseada para activar su servicio , 
al realizar el pago, Yappy se generara un documento virtual que contiene los datos generales de su transacción, guarde ese documento en su celular u ordenador.</p>
<br> 

<p><b>OBSERVACION : Copiar el código de "Confirmación" del documento de transacción o pago</b></p>
<p>Tal como se describe en el "primer paso" cada pago o transacción tiene datos generales de la transacción como nombre, fecha, cantidad , confirmación etc.</p>
<p><em>Importante!!!</em> Guarde, mantenga o copie de la transacción el denominado código de "Confirmación" descrito en el documento de pago, para utilizarlo luego en el App Super Móvil.</p>
<p>Dicho código es fácil distinguirlo porque consta de LETRAS-NUMEROS y dice "CONFIRMACION".</p>

<br>

<p><b>SEGUNDO PASO : Ingresar el código de "Confirmación" en SUPER MOVIL CHOFER</b></p>
<p>Ingrese este código en el aplicativo en la opción 3 puntos(...) "Activar servicio" a la derecha superior del app , desde aquí podrá activar el servicio con el código de "Confirmación" de su pago</p>
<p>Al momento de instalar por primera vez el APP SUPER MOVIL CHOFER , el sistema automáticamente le llevara a la opción "Activar Servicio", dado que es primera vez y necesita ser activado con la "Confirmación" del prepago realizado</p>
<p>Si usted ya es usuario de SUPER MOVIL CHOFER y se quedo sin saldo, recuerde !!! que no podrá "Conectarse" para brindar servicios a usuarios, debe realizar los pasos "UNO" y "DOS" para activar el servicio.</p>
<p>Para estar pendiente de su saldo ,puede verificar su estado en cuenta en la opción de 3 puntos(...) a la derecha de la parte superior del APP en "Estado de Cuenta".</p>

<br>

<p><b>PROBLEMAS O ERRORES CON LA ACTIVACION</b></p>

<p>Por favor siga las siguientes recomendaciones :</p>
<p><b>1. Anote o Escriba la confirmación en SUPER MOVIL, tal como esta escrita en el documento virtual de pago Yappy</b></p>
<p>-  Ingrese la confirmación sin cometer errores de omisión de alguna letra o numero y escriba el código en mayúscula</p>
<p>-  Espera el menos 4 minutos, después de realizar el pago con Yappy y luego introduzca su código de confirmación en Super Móvil</p>

